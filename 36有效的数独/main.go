func isValidSudoku(board [][]byte) bool {
	rows := [9][9]byte{}
	columms := [9][9]byte{}
	blocks := [9][9]byte{}
	for i := 0; i < 9; i++ {
		for j := 0; j < 9; j++ {
			char := board[i][j] - '1'
			if char >= 0 && char <= 9 {
				rows[i][char]++
				if rows[i][char]>1{
					return false
				}
				columms[j][char]++
				if columms[j][char]>1{
					return false
				}
				blocks[(i/3)*3+j/3][char]++
				if blocks[(i/3)*3+j/3][char]>1{
					return false
				}

			}

		}
	}
	return true
}

